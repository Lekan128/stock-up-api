CREATE TABLE IF NOT EXISTS store(
    id uuid NOT NULL PRIMARY KEY,
    address VARCHAR(255),
    name VARCHAR(64) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS users(
    id UUID NOT NULL PRIMARY KEY,
    email VARCHAR(64) NOT NULL,
    first_name VARCHAR(64) NOT NULL,
    last_name VARCHAR(64),
    password VARCHAR(255) NOT NULL,
    phone_number VARCHAR(18),
    role VARCHAR(16) NOT NULL,
    store_id UUID REFERENCES store
);

CREATE TABLE IF NOT EXISTS token(
    id UUID NOT NULL PRIMARY KEY,
    user_id UUID REFERENCES users ON DELETE CASCADE,
    value VARCHAR(255) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS category(
    id uuid NOT NULL PRIMARY KEY,
    name VARCHAR(32) UNIQUE NOT NULL
);

CREATE TABLE IF NOT EXISTS product(
    id uuid NOT NULL PRIMARY KEY,
    name VARCHAR(128) NOT NULL,
    category_id UUID REFERENCES category,
    selling_price DECIMAL(13, 2) NOT NULL,
    cost_price DECIMAL(13, 2),
    number_available INTEGER NOT NULL,
    description VARCHAR(255),
    image_url TEXT,
    store_id UUID NOT NULL REFERENCES store,
    created_at TIMESTAMP WITHOUT TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITHOUT TIME ZONE DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS tag(
    id INT NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    name VARCHAR(255) NOT NULL UNIQUE,
    CONSTRAINT tag_pkey PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS product_tag(
    product_id UUID NOT NULL REFERENCES product ON DELETE CASCADE,
    tag_id INT NOT NULL REFERENCES tag ON DELETE CASCADE,
    CONSTRAINT product_tag_pkey PRIMARY KEY (product_id, tag_id)
);
